using System;
using System.Threading.Tasks;

namespace ApplicationLogic.Base
{
    public static class TaskUtils
    {
        public static async void FireAndForgetSafeAsync(this Task task, IErrorHandler handler = null)
        {
            try
            {
                await task;
            }
            catch (Exception ex)
            {
                handler?.HandleError(ex);
            }
        }
    }
}
